#OPERADORES:

# + = suma
# - = resta
# * = multiplicacion
# / = division 
# // = division entera( solo toma la parte entera)
# % = residuo de la division
# ** = potenciacion ( exponenciación)

#Ejemplos:

print("4"+"5") #suma entre cadenas

print(2**3)

print(2**3.)

print(2.**3)

print(2.**3.)

print(6 // 3) # = 2

print(6// 3.) # = 2.0

print(6.//3) # = 2.0

print(6.//3.) # = 2.0

print(14%4) # = 2

print(3*5+2) # (tener un orden en las operaciones)

print((2+3)*5) # Además python respetar los parentesis


'''
TIPOS DE LITERALES
• Enteros -----> números sin decimales
• Flotantes----> números con decimales
• Cadenas------> texto entre comillas
• Valores Booleanos--> True o False

#print(type(_)): tipo de clase o tipos de literales

'''


#ENTERO
print(type(2))#class"int"

#FLOTANTE
print(type("2")) #class"str"

#DECIMAL
print(type(2.5))#class"float"

#BOOLEANO
##print(type)->boolean:
print(type(True))#class bool
print(type(False))#class bool

#EJEMPLOS: 

print(11_111_111) #ENTEROS:usas los guiones para separar que sirve para ordenar

print(2.5) #FLOTANTES:son los decimales, se usa el punto

print("hola","52") #CADENAS:son las cadenas de literales,STRINK

print(True) #VALOR BOOLEANO: para representar un valor de verdadero

print(False) #VALOR BOOLEANO: para representar un valor de falso